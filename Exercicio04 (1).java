import java.util.Scanner;

public class Exercicio04 {
    
    public static void calcularTabuada(int num){
        for (int i = 1; i <=10; i++) {
            double resultado = num*i;
            System.out.println(num+ " x "+ i+ " = "+resultado);
           
        }
       
    }
    
}
